namespace Editor
{
    /// <summary>
    /// This class basically serves as a Key/Value pair with string/float types.
    /// </summary>
    public class MultiExpressionProgramming
    {
        public string Key;
        public float Value;

        public MultiExpressionProgramming() 
        {
        }

        public MultiExpressionProgramming(string key, float value)
        {
            Key = key;
            Value = value;
        }
    }
}
